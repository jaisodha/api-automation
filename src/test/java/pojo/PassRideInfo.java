package pojo;

public class PassRideInfo {

    private float count;
    private String text;
    private String type;


    // Getter Methods

    public float getCount() {
        return count;
    }

    public String getText() {
        return text;
    }

    public String getType() {
        return type;
    }

    // Setter Methods

    public void setCount(float count) {
        this.count = count;
    }

    public void setText(String text) {
        this.text = text;
    }

    public void setType(String type) {
        this.type = type;
    }
}
