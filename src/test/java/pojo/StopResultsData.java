package pojo;

public class StopResultsData {
    private String address;
    private String id;
    private float lat;
    private float lng;
    private String name;

    public String getAddress() {
        return address;
    }

    public String getId() {
        return id;
    }

    public float getLat() {
        return lat;
    }

    public float getLng() {
        return lng;
    }

    public String getName() {
        return name;
    }
}
