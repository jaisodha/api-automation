package pojo;

public class UserSubDataSubscriptionDetails {
    private String amount;
    private String applicability;
    private String info_text;
    private String name;
    private boolean renewable;
    private String rides_count;
    private String state;
    private String sub_title;
    private String subscription_id;
    private String subscription_type;
    private String title;
    private String ui_template;
    private String user_subscription_id;
    private String validity_days;

    public String getAmount() {
        return amount;
    }

    public String getApplicability() {
        return applicability;
    }

    public String getInfo_text() {
        return info_text;
    }

    public String getName() {
        return name;
    }

    public boolean getRenewable() {
        return renewable;
    }

    public String getRides_count() {
        return rides_count;
    }

    public String getState() {
        return state;
    }

    public String getSub_title() {
        return sub_title;
    }

    public String getSubscription_id() {
        return subscription_id;
    }

    public String getSubscription_type() {
        return subscription_type;
    }

    public String getTitle() {
        return title;
    }

    public String getUi_template() {
        return ui_template;
    }

    public String getUser_subscription_id() {
        return user_subscription_id;
    }

    public String getValidity_days() {
        return validity_days;
    }
}
