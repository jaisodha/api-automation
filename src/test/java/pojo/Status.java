package pojo;

public class Status {
    private String text;
    private String textColor;


    // Getter Methods

    public String getText() {
        return text;
    }

    public String getTextColor() {
        return textColor;
    }

    // Setter Methods

    public void setText(String text) {
        this.text = text;
    }

    public void setTextColor(String textColor) {
        this.textColor = textColor;
    }

}
